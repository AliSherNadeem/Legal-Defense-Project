<p style="text-align:justify;  
    text-justify:auto; color: black;">When you’re scouring the net for a sector-specific legal resource, or for trustworthy insights into the latest legal developments, the sheer array of law blogs on offer can seem overwhelming.

That doesn’t change the fact that there has never been more great content targeted toward legal professionals on the web. Everyone, from in-house operations to intellectual property specialists to criminal lawyers, is well-accounted for. To save you some time, we’ve put together a list of 30 top legal blogs that you need to follow, neatly separated by sector.<br>
Finding the right resources as a corporate counsel professional can be tricky — here are the best blogs for an in-house legal team like yours.<br>
Above the Law remains the go-to option when it comes to in-house blog reading. They run a neatly designed site with well-contextualized material that’s comprehensive across the legal landscape.<br>This comprehensive resource covers stories in categories including ethics & compliance, litigation, law department management, technology, human resources, and careers in legal. Back issues of their online magazine, Digital Docket, are archived on the site. With columns such as Learn Your World, which explores country-specific facts and opinion columns such as My Take, the magazine has something to peak everyone’s interest.<br><br><br>
<h2>Legal Operations Blogs<h2>


<h4>CLOC</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">The Corporate Legal Operations Consortium (CLOC) blog is great for catching up on best practices in legal operations. They publish high-end content at a good frequency, and their site is chock-full of choice auxiliaries too, including conference information and year-end reports.</p>

<h2>Top legal technology blogs</h2>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Technology never stops evolving. Legal professionals that fail to stay up-to-date on the latest in legal tech will miss out on incredible tools and powerful solutions that can support their work. The following blogs take care of the heavy-lifting by investigating, reviewing, and reporting on noteworthy innovations.</p>

<h4>LawSites</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">You may recognize the founder of this blog, Robert Ambrogi, from his work as a columnist for Above the Law and the ABA Journal. LawSites focuses specifically on legal technology and offers insights into new legal tech products and websites. Don’t forget to tune into LawNext, the accompanying podcast that features innovators and entrepreneurs in the legal industry on each week’s episode.</p>

<h4>Law Technology Today</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">This blog is published by the ABA Legal Technology Resource Center (LTRC) and strives to provide guidance on how to leverage legal technology in a variety of legal practice settings. Look to them for quick legal tech tips, case studies, and a wide range of recommended resources including books, videos, and podcasts.</p>

<h4>ABA TECHSHOW</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Although the the ABA TECHSHOW only happens once a year, the blog is updated frequently. The site features guest authors that provide news and tips about everything from social media marketing for legal professionals to managing cybersecurity and data privacy.</p>

<h4>Top legal news blogs</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Whether you want to do research for an upcoming case, dig into in-depth legal analytics pieces, or discover upcoming legal industry events, legal news blogs can simplify your search. Check out these go-to legal news blogs whose curated content will keep you abreast of all these legal.</p>

<p4>Law.com</p4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Think of Law.com as your all-inclusive legal newspaper. Here you can find trending legal stories, the LegalSpeak podcast, and press releases. Plus, you can access useful tools such as their verdict search, legal dictionary, and legal communities directory.
</p>
<h4>Legal Desire</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Legal Desire is a beautifully designed website with a full range of legal news split into in-house and law firm, deals, and analysis sections. It’s also a great resource for those interested in networking with the greater legal community, offering a frequently updated list of legal events to attend. There’s even a section dedicated to job listings if you’re looking for a new opportunity!</p>

<p4>Legal Reader</p4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Legal Reader is a legal news blog with an uncompromising ethos. They focus on the protection of consumers from corporate malpractice, making them an excellent resource for legal news junkies and corporate law specialists alike.</p>

<h2>Top business law blogs</h2>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Business law is a broad category that encompasses both public and private law. Blogs in this space cover topics such as small business law, intellectual property, contract negotiations, and more. Take a look at these popular business law blogs, each of which has their own speciality.</p>

<h2> Illinois Business Law Journal</h2>
<p style="text-align:justify;  
    text-justify:auto; color: black;">The IBLJ is an “independent peer-reviewed, student publication of the University of Illinois College of Law,” and their blog specializes in the intersection of business and law. They publish content in quarterly clusters, and because part of their stated aim is “to give authors a creative and analytical outlet for legal areas that interest them,” their work is often highly unique and passionately thorough.</p>

<h4>Strictly Business</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">If you deal with the tech and startup space, then Strictly Business could well be the perfect blog for you. Edited by Alexander J. Davie, an attorney at Riggs Davie PLC based in Nashville, Tennessee, the blog deals comprehensively with the entrepreneurial, startup, venture capital, and private fund space.</p>

<h2>Law 4 Small Business</h2>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Small business law is impressively well-accounted for in the blogosphere, and Law 4 Small Business is one of the very best outlets of its kind. If you deal with small businesses, this blog is a must, offering from-the-field reports on the latest small business cases.</p>

<h2>The IP Law Blog</h2>
<p style="text-align:justify;  
    text-justify:auto;">A must for legal professionals specializing in IP (intellectual property), The IP Law Blog provides deeper insights into developments in patent, copyright, trademark, and other intellectual property-related areas.</p>

<h2>Top criminal law blogs</h2>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Criminal law cases may dominate generate newspaper headlines, but finding insightful content written by and for legal professionals can be difficult. These top blogs tackle topics such as state versus federal criminal law, precedent-setting verdicts, and moral debates.</p>

<h4>The Crime Report</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">The criminal justice resource of choice, The Crime Report is non-partisan, and their hugely prolific rate of output (50+ articles a week) gives them more of a magazine-type air than just another blog. They boast a comprehensive sweep of subject areas, from policing to drug law, hate crimes, and wrongful convictions.</p>

<h2>Crime & Consequences Blog</h2>
<p style="text-align:justify;  
    text-justify:auto; color: black;">A justified complaint you often hear from criminal law specialists is that most high-quality online content tends to be defense-oriented. That’s what makes Crime & Consequences so easy to recommend. They’re perhaps the best non-defense-oriented legal blog on the internet, offering a balanced content diet from both sides of the legal equation.</p>

<h4>The Volokh Conspiracy</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">Staffed mainly by law professors, The Volokh Conspiracy is an immensely readable and sometimes controversial blog analyzing the full gamut of U.S. criminal law.</p>

<h2>Top law firm blogs</h2>
<p style="text-align:justify;  
    text-justify:auto; color: black;">While many law firm blogs focus on their own announcements and legal specialties, others offer an impressive collection of content. On these top law firm blogs, you can discover educated opinion pieces, legal research materials, and even trending news.</p>

<h4>Kirkland & Ellis</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">As one of the most internationally respected law firms, Kirkland & Ellis operates a stylishly designed blog with a highly functional search interface. Their content is snackable in length and ordered according to “trending topics” — you can find good quality stuff on everything from cybercrime to economic sanctions and tax reform here.
</p>
<h4>Latham & Watkins</h4>
<p style="text-align:justify;  
    text-justify:auto;">It seems inaccurate to say that Latham & Watkins runs a blog when, in fact, they run a whole matrix of subject- and sector-specific legal blogs on their site. From Middle East developments to environmental law to fintech topics, L&W has an article for it all, making them an excellent one-stop-shop when you’re hunting for legal resources.
</p>
MyShingle</h4>
<p style="text-align:justify;  
    text-justify:auto; color: black;">If you’re working on a slightly smaller scale, then MyShingle could be a great option for a law firm blog. They’re the most comprehensive online resource for smaller firms and independent legal professionals.
</p>
