1.4.3 Limitations and Obstacles
It has to be noted that upon commencement of this assessment, the authors were not
furnished with any basic documents, records, or other information relating to current laws
and regulations relating to trafficking in persons in Pakistan. Specifically, no official copies of
the Prevention and Control of Human Trafficking Ordinance 2002 (Pakistan), the Prevention
and Control of Human Trafficking Rules 2004 (Pakistan) were made available to the authors.
As this report was completed exclusively outside Pakistan, the authors were unable to access
supplementary material such as parliamentary bills, explanatory memoranda, and were
unable to consult with experts on Pakistani law, with international organizations, and nongovernment organizations.
The combination of these factors places obvious constraints on the ability to
comprehensively analyze Pakistan’s laws and regulations relating to trafficking in persons.
Accordingly, the information presented in this report should be considered as an indicative
snapshot rather than a detailed account of laws and regulations relating to trafficking in
persons in Pakistan.


